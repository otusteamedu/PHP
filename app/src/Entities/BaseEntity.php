<?php


namespace App\Entities;


abstract class BaseEntity
{
    protected string $id;

    /**
     * @return string
     */
    public function getId(): string
    {
        return $this->id;
    }

    /**
     * @param string $id
     */
    public function setId(string $id): void
    {
        $this->id = $id;
    }

    abstract public function toArray() : array;
}