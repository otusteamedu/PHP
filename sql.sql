-- The most profitable movie
select film_id, film_name, sum
from (
         select tFilm.id                        as film_id,
                tFilm.name                      as film_name,
                sum(tBasket.price)              as sum,
                max(sum(tBasket.price)) over () as max_sum
         from cinema.basket as tBasket
                  join cinema.film as tFilm on tBasket.film_id = tFilm.id
                  join cinema.order as tOrder on tBasket.order_id = tOrder.id
         where order_id is not null
           and tOrder.date_pay is not null

         group by tFilm.id
         order by sum desc
     ) profitable_movie
where sum = max_sum;
;

