<?php

namespace sockets;


final class Client
{
    private Socket $socket;

    public function __construct(string $host, int $port)
    {
        $this->initSocket($host, $port);
    }

    public function waitForMessage()
    {
        echo "Enter Message:\t";
        $this->message($this->readline());
    }

    private function message(string $message)
    {
        $this->socket->write($message);
        $this->waitingResponse();
    }

    private function initSocket(string $host, int $port): void
    {
        $this->socket = new Socket($host, $port);
        $this->socket->create();
        $this->socket->connect();
    }

    private function readline(): string
    {
        return rtrim(fgets(STDIN));
    }

    public function waitingResponse()
    {
        echo "Server says:\t" . $this->socket->read();
    }

    public function run()
    {
        do {
            echo "Enter Message:\t";
            $this->socket->write($this->readline());

            $message = $this->socket->read();
            echo "Server says:\t" . $message . "\n\n";

        } while (true);
    }
}