<?php


namespace Tests\_support\Model;


class OrderPositionModel extends JsonDataDriver implements ModelInterface
{
    /* состояние */
    public $state;
    /* заказ */
    public $orderNumber;
    /* клиент */
    public $client;
    /* артикул */
    public $article;
    /* наименование */
    public $name;
    /* производитель */
    public $brand;
    /* ref */
    public $ref;

